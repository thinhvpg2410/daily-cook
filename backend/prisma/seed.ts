async function main() {
  console.log("🌾 Seeding DailyCook AI database...");

  // Create ingredients (use upsert to avoid duplicates)
  const ingredientMap = new Map<string, string>();
  for (const i of ingredients) {
    const ing = await prisma.ingredient.upsert({
      where: { name: i.name },
      update: {},
      create: i,
    });
    ingredientMap.set(i.name, ing.id);
  }

  // Create demo user
  const user = await prisma.user.upsert({
    where: { email: "demo@dailycook.vn" },
    update: {},
    create: {
      email: "demo@dailycook.vn",
      name: "Demo User",
      phone: "+84999999999",
      passwordHash: "demo",
      preference: {
        create: {
          gender: "male",
          age: 26,
          height: 172,
          weight: 67,
          goal: "maintain",
          activity: "medium",
          dailyKcalTarget: 2200,
        },
      },
    },
  });

  // Delete existing recipes first to avoid duplicates
  await prisma.recipeItem.deleteMany({});
  await prisma.recipe.deleteMany({});

  // Create recipes
  for (const r of recipes) {
    const items = r.ingredients.map((i) => ({
      ingredientId: ingredientMap.get(i.name)!,
      amount: i.amount,
      unitOverride: i.unit,
    }));

    // Calculate totalKcal, protein, fat, carbs
    let totalKcal = 0;
    let totalProtein = 0;
    let totalFat = 0;
    let totalCarbs = 0;
    for (const i of r.ingredients) {
      const ref = ingredients.find((x) => x.name === i.name);
      if (ref) {
        const multiplier =
          i.unit === "quả" || i.unit === "cái" ? 1 : i.amount / 100;
        totalKcal += ref.kcal * multiplier;
        totalProtein += (ref.protein || 0) * multiplier;
        totalFat += (ref.fat || 0) * multiplier;
        totalCarbs += (ref.carbs || 0) * multiplier;
      }
    }

    await prisma.recipe.create({
      data: {
        authorId: user.id,
        title: r.title,
        description: r.description,
        region: r.region,
        cookTime: r.cookTime,
        likes: Math.floor(500 + Math.random() * 2000),
        totalKcal: Math.round(totalKcal),
        protein: Math.round(totalProtein * 10) / 10,
        fat: Math.round(totalFat * 10) / 10,
        carbs: Math.round(totalCarbs * 10) / 10,
        tags: r.tags,
        image: r.image,
        steps: r.steps as Prisma.InputJsonValue,
        items: { create: items },
      },
    });
  }

  console.log("✅ Seed completed successfully!");
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
